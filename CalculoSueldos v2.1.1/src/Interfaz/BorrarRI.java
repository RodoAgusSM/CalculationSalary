    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int cont = 0;
            if (miModelo.borrarReciboIndependiente(txtBorrar.getText().toUpperCase())) {
                JOptionPane.showMessageDialog(this, "Recibo independiente eliminado con exito");
                txtBorrar.setText(null);
                cont++;
        }
        if (cont == 0) {
            JOptionPane.showMessageDialog(this, "No existe un recibo independiente con ese nombre");
            txtBorrar.setText(null);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

